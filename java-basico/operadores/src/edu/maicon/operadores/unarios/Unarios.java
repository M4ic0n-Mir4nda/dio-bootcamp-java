package edu.maicon.operadores.unarios;

public class Unarios {
	public static void main(String[] args) {
		int numero = 5;
		
		numero = - numero; // Nega o valor ou seja torna ele negativo
		
		System.out.println(numero);
		
		numero = numero * -1; // Torna ele positivo novamente
		
		System.out.println(numero);
	}
}
